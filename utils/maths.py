from math import gcd, lcm

if __name__ == "__main__":
    # greatest common divisor
    print(f"greatest common divisor gcd of 10 and 8: {gcd(10, 8)}")
    print(f"greatest common divisor gcd of 25 and 10 and 5: {gcd(25, 10, 5)}")
    print()
    # least common multiple
    print(f"least common multiple lcm of 10 and 8: {lcm(10, 8)}")
    print(f"least common multiple lcm of 25, 10 and 5: {lcm(25, 10, 5)}")
